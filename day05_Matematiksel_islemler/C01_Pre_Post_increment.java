package JAVA_82.day05_Matematiksel_islemler;

public class C01_Pre_Post_increment {
    public static void main(String[] args) {

        int sayi=10;
        System.out.println("Pre-İncr :"+ ++sayi);  // 11
        System.out.println("Pos-İncr :"+ sayi++);  // 11 yazar sonra 12 olur

        System.out.println("Pos-İncr Sonra :"+ sayi); // 12 yazar



    }

}
