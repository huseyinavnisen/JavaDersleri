package JAVA_82.day04_dataCasting;

public class C05_Odev_1 {
    public static void main(String[] args) {
        byte s=111;

        short ss=s;
        System.out.println("Short Karşılığı : "+ ss);

        int is=s;
        System.out.println("İnteger Karşılığı : "+ is);

        float fs=s;
        System.out.println("Float Karşılığı : "+ fs);

        double ds=s;
        System.out.println("Float Karşılığı : "+ ds);

    }
}
